class Estudiant:
    def __init__(self, nom, nota):
        self.nom = nom
        self.nota = nota

    def ha_aprovat(self):
        return self.nota >= 5

# Exemple d'ús
estudiant1 = Estudiant("Maria", 7)
print(f"{estudiant1.nom} ha aprovat:", estudiant1.ha_aprovat())
